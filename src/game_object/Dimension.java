package game_object;

public class Dimension {

	private double myWidth;
	private double myHeight;
	
	public double getWidth() {
		return myWidth;
	}
	
	public void setWidth(double width) {
		myWidth = width;
	}
	
	public double getHeight() {
		return myHeight;
	}
	
	public void setHeight(double height) {
		myHeight = height;
	}
	
}
